function tCat(category) {
  if (!category) return category;
  const canonical = CANONICAL_CAT[category.toLowerCase().trim()] || category;
  const lang = getLang();
  return (CAT_DISPLAY[lang] && CAT_DISPLAY[lang][canonical]) || canonical;
}

function titleCase(str) {
  if (!str) return '';
  return str.replace(/\w\S*/g, w => w.charAt(0).toUpperCase() + w.slice(1).toLowerCase());
}

function setLang(lang) {
  localStorage.setItem('lang', lang);
  applyTranslations();
  document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}

function applyTranslations() {
  const lang = getLang();

  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });

  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });

  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  fillCategoryDatalist('category-list');
}

document.addEventListener('DOMContentLoaded', applyTranslations);

const CATEGORY_LIST = [
  'Appetizer','Bakery','Bread','Breakfast','Cake','Chicken','Desserts',
  'Drink','Fish','Main Course','Meat','Pasta','Rice','Salad','Sandwich',
  'Seafood','Snack','Soup','Vegan','Vegetarian'
];

function fillCategoryDatalist(id) {
  const dl = document.getElementById(id);
  if (!dl) return;
  const sorted = CATEGORY_LIST.map(c => tCat(c)).sort((a, b) => a.localeCompare(b));
  dl.innerHTML = sorted.map(c => `<option value="${c}">`).join('');
}

// Returns the translated field for the active language, falling back to the original.
// e.g. localizedField(recipe, 'title') → recipe.title_fr (if lang=fr and it exists)
function localizedField(obj, field) {
  const lang = getLang();
  const key = `${field}_${lang}`;
  return (obj[key] && obj[key].trim()) ? obj[key] : obj[field];
}

